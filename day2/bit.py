# 按位与运算 &
'''
    1011
    0010
   ------
    0010
'''
i = 11 # ob1011
j = 2  # 0b10
z = i & j
print(z)

# 按位或运算 |
'''
    1011
    0010
   ------
    1011
'''
i = 11
j = 2
z = i | j
print(z)

# 按位异或运算 ^
'''
    1011
    0010
   ------
    1001
'''
i = 11
j = 2
z = i ^ j
print(z)

# 按位取反运算 ～
i = 11
z = ~i
print(z)


